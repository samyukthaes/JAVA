public class AssignmentOp{
    public static void main(String[] args){

        int num1=2;
       
        int result1= num1++; //2 //value is fetched and then incremented
        int result2= ++num1; //3 //value is incremented and then fetched

        System.out.println(result1);
        System.out.println(result2);
        
            
    }
}