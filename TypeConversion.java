public class TypeConversion{
    public static void main(String[] args){

        
        byte b=127;
        int a=b;
        System.out.println(a);

        int c=257;
        byte d=(byte)c;
        System.out.println(d);

        float f=5.6f;
        int e=(int)f;
        System.out.println(e);

        
    }

}